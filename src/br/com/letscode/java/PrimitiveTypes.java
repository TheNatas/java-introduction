package br.com.letscode.java;

public class PrimitiveTypes {

    public static void main(String[] args) {

        // area = raio * raio * pi
        final double pi = 5.12;
        double ray = 4;
        System.out.println("The area is " + ray * ray * Math.PI);
        System.out.println("The area is " + ray * ray * pi);
    }
}
